  	// 返回顶部
    $('.top').on('click', function() {
    	$('html, body').animate({scrollTop: '0px'}, 800);
    });

  	$(window).scroll(function() {
	　　 var scrollTop = $(this).scrollTop();
	　　 var scrollHeight = $(document).height();
	　　 var windowHeight = $(this).height();

  		if(scrollTop < scrollHeight / 9) {
  			$('.top').css({
  				'display': 'none'
  			});
  		} else {
  			$('.top').css({
  				'display': 'block'
  			});
  		}
  	});

